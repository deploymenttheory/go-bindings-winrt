// Package fileasm assembles generated Go source files: the DO-NOT-EDIT
// header, build tag, package clause, grouped imports, and body — then formats
// the result with go/format. Every emitter funnels file output through
// Assemble so scaffold changes happen in exactly one place.
package fileasm

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

// File is one Go source file awaiting assembly.
type File struct {
	// PackageName is the package clause name.
	PackageName string
	// BuildTag is the //go:build expression.
	BuildTag string
	// Imports maps alias → import path. Aliases equal to the path's last
	// element are emitted without an explicit alias.
	Imports map[string]string
	// Body is the file content below the imports.
	Body string
}

// Header marks files this module's generator owns; the emitter only ever
// prunes or overwrites files that carry it.
const Header = "// Code generated by go-bindings-winrt-codegen. DO NOT EDIT."

// GeneratedBuildTag is the //go:build constraint every generated file carries.
// Struct layouts and syscall marshaling are computed for the 64-bit LLP64
// model, which Windows amd64 and arm64 share — so one set of bindings is
// correct on both. It deliberately excludes 386 (32-bit pointers, distinct
// layouts), whose support would require a separate per-arch emission pass.
const GeneratedBuildTag = "windows && (amd64 || arm64)"

// Assemble renders and gofmts the file.
func Assemble(file File) ([]byte, error) {
	var buffer bytes.Buffer
	buffer.WriteString(Header + "\n")
	fmt.Fprintf(&buffer, "\n//go:build %s\n\n", file.BuildTag)
	fmt.Fprintf(&buffer, "package %s\n\n", file.PackageName)
	writeImports(&buffer, file.Imports)
	buffer.WriteString(file.Body)

	formatted, err := format.Source(buffer.Bytes())
	if err != nil {
		return nil, fmt.Errorf("fileasm: generated %s does not parse: %w", file.PackageName, err)
	}
	return formatted, nil
}

// writeImports emits the import block in two groups: standard library, then
// module-internal (matching goimports grouping).
func writeImports(buffer *bytes.Buffer, imports map[string]string) {
	if len(imports) == 0 {
		return
	}
	var stdlib, internal []string
	for alias, path := range imports {
		line := fmt.Sprintf("%s %q", alias, path)
		if lastSegment(path) == alias {
			line = fmt.Sprintf("%q", path)
		}
		if strings.Contains(path, ".") {
			internal = append(internal, line)
		} else {
			stdlib = append(stdlib, line)
		}
	}
	sort.Strings(stdlib)
	sort.Strings(internal)

	buffer.WriteString("import (\n")
	for _, line := range stdlib {
		fmt.Fprintf(buffer, "\t%s\n", line)
	}
	if len(stdlib) > 0 && len(internal) > 0 {
		buffer.WriteString("\n")
	}
	for _, line := range internal {
		fmt.Fprintf(buffer, "\t%s\n", line)
	}
	buffer.WriteString(")\n\n")
}

func lastSegment(path string) string {
	if i := strings.LastIndex(path, "/"); i >= 0 {
		return path[i+1:]
	}
	return path
}

// WriteGoFile assembles the file and writes it to path, creating parent
// directories as needed.
func WriteGoFile(path string, file File) error {
	formatted, err := Assemble(file)
	if err != nil {
		return err
	}
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return fmt.Errorf("fileasm: %w", err)
	}
	if err := os.WriteFile(path, formatted, 0o644); err != nil {
		return fmt.Errorf("fileasm: %w", err)
	}
	return nil
}
